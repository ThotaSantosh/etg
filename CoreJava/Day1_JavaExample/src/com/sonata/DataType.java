package com.sonata;

public class DataType {

	public static void main(String[] args) {
		int a=10000;
		System.out.println(a);//print the integer data type
		short myShort=1000;
		System.out.println(myShort);//print the short data type
		boolean b = false;
		System.out.println(b);//print the boolean data type
		char c1=(char)'a';
		System.out.println(c1);//print the char data type
		long lo=250000;
		System.out.println(lo);//print the long data type
		float f=(float)25.130;
		System.out.println(f);//print the float data type
		byte b1=10;
		System.out.println(b1);//print the byte data type
		double d=2000000;
		System.out.println(d);//print the double data type
	}

}
